package Servlet;




public class emp {
    
    int empID;
    String name;
    String dept;
    int sal;
    int bal;
    
    public int getEmpID() {
        return empID;
    }

    public void setEmpID(int empID) {
        this.empID = empID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDept() {
        return dept;
    }

    public void setDept(String dept) {
        this.dept = dept;
    }

    public int getSal() {
        return sal;
    }

    public void setSal(int sal) {
        this.sal = sal;
    }
    
    
    public int getBal() {
        return bal;
    }

    public void setBal(int bal) {
        this.bal = bal;
    }
            
}
